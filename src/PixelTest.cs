    public static void DrawText(
        SpriteBatch spriteBatch,
        Texture2D pixel,
        string text,
        Vector2 position,
        int scale,
        Color color)
    {
        text = text.ToUpperInvariant();

        int cursorX = (int)position.X;
        int cursorY = (int)position.Y;

        foreach (char character in text)
        {
            if (character == ' ')
            {
                cursorX += 4 * scale;
                continue;
            }

            if (!Font.TryGetValue(character, out string[]? pattern))
            {
                cursorX += 6 * scale;
                continue;
            }

            for (int row = 0; row < pattern.Length; row++)
            {
                for (int col = 0; col < pattern[row].Length; col++)
                {
                    if (pattern[row][col] == '1')
                    {
                        var rectangle = new Rectangle(
                            cursorX + col * scale,
                            cursorY + row * scale,
                            scale,
                            scale
                        );

                        spriteBatch.Draw(pixel, rectangle, color);
                    }
                }
            }

            cursorX += 6 * scale;
        }
    }

    public static Vector2 MeasureText(string text, int scale)
    {
        int width = 0;

        foreach (char character in text)
        {
            width += character == ' ' ? 4 * scale : 6 * scale;
        }

        return new Vector2(width, 7 * scale);
    }

    public static void DrawCenteredText(
        SpriteBatch spriteBatch,
        Texture2D pixel,
        string text,
        int screenWidth,
        int y,
        int scale,
        Color color)
    {
        Vector2 size = MeasureText(text, scale);
        float x = (screenWidth - size.X) / 2f;

        DrawText(spriteBatch, pixel, text, new Vector2(x, y), scale, color);
    }
